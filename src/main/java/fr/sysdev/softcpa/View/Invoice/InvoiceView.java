    private void search() {
          String s = jTextField_Invoice_Search.getText();

        if (s.equals("")) {
            invoices.clear();
            invoices.addAll(allInvoices);
            bindingInvoicesTable();
           // searchInterface();
        } else {
            List<Invoice> filter = InvoicesPredicates.filterInvoices(allInvoices, InvoicesPredicates.invoiceNumberContains(s));
            invoices.clear();
            invoices.addAll(filter);

            if (invoices.size() > 0) {

               // updateInterface();
                bindingInvoicesTable();
                jTable_Invoice.setRowSelectionInterval(0, 0);
                jTable_Invoice.setSelectionBackground(Color.blue);
                jTable_Invoice.setSelectionForeground(Color.red);

            } else {

                invoices.clear();
                bindingInvoicesTable();
              //  searchInterface();

            }
    }
}

