    public void addFrame(JInternalFrame view) {
        if (!viewsMap.containsKey(view.getTitle())) {
            
            viewsMap.put(view.getTitle(), view);
            view.setVisible(true);
            jDesktopPane.add(view);

            try {
                view.setSelected(true);
            } catch (java.beans.PropertyVetoException e) {
                JOptionPane.showInputDialog(null, "selection");
            }
        } else {
           
            {try {
                
                view.setSelected(true);
                
            } catch (PropertyVetoException ex) {
                Logger.getLogger(SoftcpaApplication.class.getName()).log(Level.SEVERE, null, ex);
            }}
           
            
        }

    }

